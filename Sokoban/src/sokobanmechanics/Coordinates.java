package sokobanmechanics;

public class Coordinates {

    private int x = 0;
    private int y = 0;

    public int getX() {  //gets the x position
        return x;
    }

    public int getY() {
        return y;  //Gets the y position
    }

    public void setX(int newX) {
        x = newX;  //Sets the x position
    }

    public void setY(int newY) {
        y = newY;  //Sets the y position
    }

}